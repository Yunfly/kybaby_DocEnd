$(function(){
    $(window).scroll(function() {
        var s = $(window).scrollTop();
        if (s > 64) {
            $("#header").removeClass("transparent").addClass('doctor_header');
        }else{
            $("#header").removeClass("doctor_header").addClass('transparent');
        }
    })
})